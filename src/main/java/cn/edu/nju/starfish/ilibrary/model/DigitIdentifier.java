package cn.edu.nju.starfish.ilibrary.model;

/**
 * The predefined enumeration of digit identifiers of documents.
 *
 * @author Haixing Hu
 */
public enum DigitIdentifier {

  /**
   * Represents the Digit Object Identifier of the document.
   */
  DOI,

  /**
   * Represents the PubMed Central ID of the document.
   */
  PUBMED_ID,

  /**
   * Represents the Google Scholar ID of the document.
   */
  GOOGLE_SCHOLAR,

  /**
   * Represents the arXiv ID of the document.
   */
  ARXIV,
}
